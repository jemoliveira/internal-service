package com.samsung.service.vo;


public class UserWsVO {
	
	private String wsUser;
	private String ascCode;
	private String wsPwd;
	private String corpCode;
	private String dev;
	private String prd;
	private String token;
	private String fsc;
	
	public String getWsUser() {
		return wsUser;
	}
	public void setWsUser(String wsUser) {
		this.wsUser = wsUser;
	}
	public String getAscCode() {
		return ascCode;
	}
	public void setAscCode(String ascCode) {
		this.ascCode = ascCode;
	}
	public String getWsPwd() {
		return wsPwd;
	}
	public void setWsPwd(String wsPwd) {
		this.wsPwd = wsPwd;
	}
	public String getCorpCode() {
		return corpCode;
	}
	public void setCorpCode(String corpCode) {
		this.corpCode = corpCode;
	}
	public String getDev() {
		return dev;
	}
	public void setDev(String dev) {
		this.dev = dev;
	}
	public String getPrd() {
		return prd;
	}
	public void setPrd(String prd) {
		this.prd = prd;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getFsc() {
		return fsc;
	}
	public void setFsc(String fsc) {
		this.fsc = fsc;
	}
	
}